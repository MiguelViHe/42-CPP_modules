#include <string>
#include <iostream>

int	main(void)
{
	std::string	str = "HI THIS IS BRAIN";
	std::string	*stringPTR = &str; //PUNTERO: puede cambiar la referencia a quien apunta. como los punteros en C.
	std::string	&stringREF = str; //REFERENCIA: exactamente como la variable. puedes cambiar el contenidoy cambiará en la variable original. NO PUEDE cambiar la referencia de la variable. se quedará siempre apuntando a la misma.

	std::cout << "Memory address of the string variable: " << &str << std::endl;
	std::cout << "Memory address held by stringPTR: " << stringPTR << std::endl;
	std::cout << "Memory address held by stringREF: " << &stringREF << std::endl;
	std::cout << std::endl;
	std::cout << "Value of the string variable: " << str << std::endl;
	std::cout << "Value pointed to by stringPTR: " << *stringPTR << std::endl;
	std::cout << "Value pointed to by stringREF: " << stringREF << std::endl;
	return (0);
}